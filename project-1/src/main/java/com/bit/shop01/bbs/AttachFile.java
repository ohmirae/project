package com.bit.shop01.bbs;

public class AttachFile {

	private int attachFileNo;
	private String filename;
	private String filetype;
	private long filesize;
	private int bbseditno;
	private int productNum;

	
	public AttachFile() {}


	public int getAttachFileNo() {
		return attachFileNo;
	}


	public void setAttachFileNo(int attachFileNo) {
		this.attachFileNo = attachFileNo;
	}


	public String getFilename() {
		return filename;
	}


	public void setFilename(String filename) {
		this.filename = filename;
	}


	public String getFiletype() {
		return filetype;
	}


	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}


	public long getFilesize() {
		return filesize;
	}


	public void setFilesize(long filesize) {
		this.filesize = filesize;
	}


	public int getBbseditno() {
		return bbseditno;
	}


	public void setBbseditno(int bbseditno) {
		this.bbseditno = bbseditno;
	}
	

	public int getProductNum() {
		return productNum;
	}


	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}
	
	


	
	
	
}
